function buscarCentroDeCustoPelaDescricao(pedacoDescricao) {
  let resultados = [];

  for (const centroDeCusto of centrosDeCusto) {
    if (centroDeCusto.descricao.toLowerCase().includes(pedacoDescricao.toLowerCase())) {
      resultados.push(centroDeCusto);
    }
  }

  return resultados;
}





