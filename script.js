function mostrarResultados() {
  const descricao = document.getElementById('descricao').value;
  const resultados = buscarCentroDeCustoPelaDescricao(descricao);

  // obter a referência do elemento que contém a tabela
  const tableContainer = document.querySelector('#table-container');

  // remover a tabela existente, se houver
  if (tableContainer.querySelector('table')) {
    tableContainer.removeChild(tableContainer.querySelector('table'));
  }

  // criar a tabela e adicionar ao container
  const table = document.createElement('table');
  tableContainer.appendChild(table);

  // adicionar uma classe para remover as bordas da tabela
  //table.classList.add('border');

  // criar o cabeçalho da tabela
  const thead = document.createElement('thead');
  table.appendChild(thead);
  const tr = document.createElement('tr');
  thead.appendChild(tr);
  const thCC = document.createElement('th');
  thCC.textContent = 'CC';
  tr.appendChild(thCC);
  const thDescricao = document.createElement('th');
  thDescricao.textContent = 'Descrição';
  tr.appendChild(thDescricao);

  // criar o corpo da tabela e adicionar as linhas com os dados
  const tbody = document.createElement('tbody');
  table.appendChild(tbody);

  for (const resultado of resultados) {
    const tr = document.createElement('tr');
    tbody.appendChild(tr);

    const tdCC = document.createElement('td');
    tdCC.textContent = resultado.codigo;
    tr.appendChild(tdCC);

    const tdDescricao = document.createElement('td');
    thDescricao.style.textAlign = 'left';
    thDescricao.style.paddingLeft = '10px';
    tdDescricao.style.textAlign = 'left';
    tdDescricao.style.paddingLeft = '10px';
    tdDescricao.textContent = resultado.descricao;
    tr.appendChild(tdDescricao);
  }
}